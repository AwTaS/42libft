char	*ft_strchar(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return ((char *) &s[i]);
		else
			i++;
	}
	if (s[i] == c)
		return ((char *) &s[i]);
	return (0);
}
